"""
data_loader.py
--------------
PyTorch Dataset and DataLoader factory for DOA estimation.

Supports two input types:
  - 'raw' : shape (2, P, T)   -- real/imag of received snapshots
  - 'cov' : shape (2, P, P)   -- real/imag of sample covariance matrix

Labels are 121-dim multi-hot vectors (DOA grid: -60..+60 deg, step 1 deg).
"""

import os
import h5py
import numpy as np
import torch
from torch.utils.data import Dataset, DataLoader, random_split


class DOADataset(Dataset):
    """
    Lazy-loading HDF5 dataset for DOA estimation.

    Parameters
    ----------
    h5_path    : str   Path to HDF5 file generated by generate_raw/cov.py
    input_type : str   'raw' or 'cov' -- used only for shape verification
    transform  : callable or None  Optional transform applied to X tensor
    """

    def __init__(self, h5_path: str, input_type: str = 'raw', transform=None):
        super().__init__()
        assert input_type in ('raw', 'cov'), "input_type must be 'raw' or 'cov'"
        assert os.path.isfile(h5_path), f'HDF5 file not found: {h5_path}'

        self.h5_path    = h5_path
        self.input_type = input_type
        self.transform  = transform

        # Read metadata without loading data
        with h5py.File(h5_path, 'r') as f:
            self.length     = f['X'].shape[0]
            self.x_shape    = f['X'].shape[1:]   # e.g. (2, 12, 16) or (2, 12, 12)
            self.num_classes = f['Y'].shape[1]    # 121

        # HDF5 file handle opened lazily per-worker
        self._file   = None
        self._dset_x = None
        self._dset_y = None

    # ── HDF5 lazy open (each DataLoader worker opens its own handle) ──────────
    def _open_if_needed(self):
        if self._file is None:
            self._file   = h5py.File(self.h5_path, 'r')
            self._dset_x = self._file['X']
            self._dset_y = self._file['Y']

    def __len__(self):
        return self.length

    def __getitem__(self, idx):
        self._open_if_needed()
        x = torch.from_numpy(self._dset_x[idx])   # float32 tensor
        y = torch.from_numpy(self._dset_y[idx])   # float32 multi-hot

        if self.transform is not None:
            x = self.transform(x)

        return x, y

    def __del__(self):
        if self._file is not None:
            self._file.close()


def get_dataloaders(
    train_h5: str,
    val_h5: str,
    test_h5: str,
    input_type: str = 'raw',
    batch_size: int = 128,
    num_workers: int = 8,
    pin_memory: bool = True,
) -> dict:
    """
    Build train / val / test DataLoaders from pre-generated HDF5 files.

    Parameters
    ----------
    train_h5, val_h5, test_h5 : str
        Paths to HDF5 datasets.
    input_type : str   'raw' or 'cov'
    batch_size : int
    num_workers : int  Parallel prefetch workers
    pin_memory : bool  Set True when training on GPU

    Returns
    -------
    dict with keys 'train', 'val', 'test' mapping to DataLoader objects.
    """
    datasets = {
        'train': DOADataset(train_h5, input_type),
        'val'  : DOADataset(val_h5,   input_type),
        'test' : DOADataset(test_h5,  input_type),
    }

    loaders = {}
    for split, ds in datasets.items():
        shuffle = (split == 'train')
        loaders[split] = DataLoader(
            ds,
            batch_size=batch_size,
            shuffle=shuffle,
            num_workers=num_workers,
            pin_memory=pin_memory,
            persistent_workers=(num_workers > 0),
            drop_last=(split == 'train'),
        )

    return loaders


